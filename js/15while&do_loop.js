// WHILE LOOP.
let i = 0
while (i <= 10) {
    console.log(i);
    i = i + 2
}

// PRINT ARRAY.
myArray = ['ram','shyam','hari']

arr = 0
while (arr < myArray.length) {
    console.log(`${myArray[arr]}`);
    
}


// DO_WHILE LOOP.  :- IST DO (PRINT)  THEN CHECK CONDITION.
let score = 1
do {
    console.log(`score is ${score}`);
    score ++
} while (score <= 10);


// ANOTHER EXAMPLE.   IST IT PRINT 11 THEN CHECK CONDITION.
let score = 11
do {
    console.log(`score is ${score}`);
    score ++
} while (score <= 10);


